#!/usr/bin/env bash
set -euo pipefail

BASE="https://backstagetalks.github.io/tennis-backstage-talks"

LINKS="H4V34N1C3D4Y177"

SOURCE_MANIFEST="H4V34N1C3D4Y170"
SOURCE_AUDIT="H4V34N1C3D4Y171"

TOP_WEB="H4V34N1C3D4Y180"
TOP_RSS="H4V34N1C3D4Y181"

TOP_RESULTS_WEB="H4V34N1C3D4Y182"
TOP_RESULTS_RSS="H4V34N1C3D4Y183"

ALL_WEB="H4V34N1C3D4Y184"
ALL_RSS="H4V34N1C3D4Y185"

ALL_RESULTS_WEB="H4V34N1C3D4Y186"
ALL_RESULTS_RSS="H4V34N1C3D4Y187"


remove_file_if_exists () {
  local TARGET="${1:-}"

  if [ -n "$TARGET" ] && [ -f "$TARGET" ]; then
    rm -f "$TARGET"
    echo "REMOVED FILE: $TARGET"
  fi
}


write_placeholder_html () {
  local FILE="${1:-}"
  local TITLE="${2:-BackstageTalks}"
  local MESSAGE="${3:-Content is not available yet.}"

  mkdir -p "$(dirname "$FILE")"

  cat > "$FILE" <<EOF
<!DOCTYPE html>
<html lang="en">
<head>
<meta charset="UTF-8">
<title>$TITLE</title>
<meta name="robots" content="noindex,nofollow,noarchive">
<meta name="viewport" content="width=device-width, initial-scale=1.0">
<style>
body {
  font-family: Arial, sans-serif;
  background: #160f0f;
  color: #f4f4f4;
  margin: 0;
  padding: 40px;
}
.container {
  max-width: 760px;
  margin: 0 auto;
}
h1 {
  font-size: 34px;
}
p {
  color: #cfcfcf;
  font-size: 18px;
}
</style>
</head>
<body>
<div class="container">
<h1>$TITLE</h1>
<p>$MESSAGE</p>
</div>
</body>
</html>
EOF
}


write_neutral_html () {
  local FILE="${1:-}"

  write_placeholder_html "$FILE" "BackstageTalks" "No public content available."
}


write_neutral_rss () {
  local FILE="${1:-}"
  local TITLE="${2:-BackstageTalks}"

  mkdir -p "$(dirname "$FILE")"

  cat > "$FILE" <<EOF
<?xml version="1.0" encoding="UTF-8"?>
<rss version="2.0">
<channel>
<title>$TITLE</title>
<link>$BASE/</link>
<description>No public content available</description>
</channel>
</rss>
EOF
}


copy_page_alias () {
  local SRC="${1:-}"
  local ALIAS="${2:-}"
  local TITLE="${3:-BackstageTalks}"

  remove_file_if_exists "public/$ALIAS"
  mkdir -p "public/$ALIAS"

  if [ -f "$SRC" ]; then
    cp "$SRC" "public/$ALIAS/index.html"
    echo "PAGE ALIAS: $SRC -> public/$ALIAS/index.html"
  else
    write_placeholder_html "public/$ALIAS/index.html" "$TITLE" "Content will be generated by the next daily run."
    echo "PAGE PLACEHOLDER: public/$ALIAS/index.html"
  fi
}


copy_rss_alias () {
  local SRC="${1:-}"
  local ALIAS="${2:-}"
  local TITLE="${3:-BackstageTalks RSS}"
  local PAGE_ALIAS="${4:-}"

  if [ -f "$SRC" ]; then
    cp "$SRC" "public/$ALIAS.xml"
    cp "$SRC" "public/$ALIAS"
    echo "RSS ALIAS: $SRC -> public/$ALIAS.xml"
  else
    cat > "public/$ALIAS.xml" <<EOF
<?xml version="1.0" encoding="UTF-8"?>
<rss version="2.0">
<channel>
<title>$TITLE</title>
<link>$BASE/$PAGE_ALIAS/</link>
<description>No content available yet</description>
</channel>
</rss>
EOF
    cp "public/$ALIAS.xml" "public/$ALIAS"
    echo "RSS PLACEHOLDER: public/$ALIAS.xml"
  fi
}


copy_json_alias () {
  local SRC="${1:-}"
  local ALIAS="${2:-}"
  local NAME="${3:-json}"

  if [ -f "$SRC" ]; then
    cp "$SRC" "public/$ALIAS"
    cp "$SRC" "public/$ALIAS.json"
    echo "JSON ALIAS: $SRC -> public/$ALIAS"
    echo "JSON ALIAS: $SRC -> public/$ALIAS.json"
  else
    cat > "public/$ALIAS" <<EOF
{
  "status": "PLACEHOLDER",
  "name": "$NAME",
  "message": "Content is not available yet."
}
EOF
    cp "public/$ALIAS" "public/$ALIAS.json"
    echo "JSON PLACEHOLDER: public/$ALIAS"
    echo "JSON PLACEHOLDER: public/$ALIAS.json"
  fi
}


add_link_card () {
  local FILE="${1:-}"
  local LABEL="${2:-Link}"
  local URL="${3:-#}"

  cat >> "$FILE" <<EOF
<div class="card">
  <div class="label">$LABEL</div>
  <a class="open-icon" href="$URL" target="_blank" rel="noopener noreferrer" title="Open link">🔗</a>
</div>

EOF
}


