write_robots () {
  cat > public/robots.txt <<EOF
User-agent: *
Disallow: /tennis.xml
Disallow: /tennis_all.xml
Disallow: /results.xml
Disallow: /all_results.xml
Disallow: /all/
Disallow: /results/
Disallow: /all_results/
Disallow: /source_manifest.json
Disallow: /source_audit.json
EOF

  echo "ROBOTS: public/robots.txt"
}


echo "===== PUBLISH LINKS / ALIASES WITH BASH ====="

mkdir -p public

copy_json_alias "public/source_manifest.json" "$SOURCE_MANIFEST" "source_manifest"
copy_json_alias "public/source_audit.json" "$SOURCE_AUDIT" "source_audit"

copy_page_alias "public/index.html" "$TOP_WEB" "BackstageTalks Tennis TOP 7"
copy_rss_alias "public/tennis.xml" "$TOP_RSS" "BackstageTalks Tennis TOP 7 RSS" "$TOP_WEB"

copy_page_alias "public/results/index.html" "$TOP_RESULTS_WEB" "BackstageTalks Tennis TOP 7 Results"
copy_rss_alias "public/results.xml" "$TOP_RESULTS_RSS" "index.html" "$ALL_WEB" "BackstageTalks Tennis ALL"
copy_rss_alias "public/tennis_all.xml" "$ALL_RSS" "BackstageTalks Tennis ALL RSS" "$ALL_WEB"

copy_page_alias "public/all_results/index.html" "$ALL_RESULTS_WEB" "BackstageTalks Tennis ALL Results"
copy_rss_alias "public/all_results.xml" "$ALL_RESULTS_RSS" "BackstageTalks Tennis ALL Results RSS" "$ALL_RESULTS_WEB"

write_link_hub
write_robots

echo "===== NEUTRALIZE PUBLIC ROOT PATHS ====="

write_neutral_html "public/index.html"
write_neutral_rss "public/tennis.xml" "BackstageTalks Tennis"
write_neutral_rss "public/tennis_all.xml" "BackstageTalks Tennis ALL"
write_neutral_rss "public/results.xml" "BackstageTalks Tennis Results"
write_neutral_rss "public/all_results.xml" "BackstageTalks Tennis ALL Results"

mkdir -p public/all public/results public/all_results

write_neutral_html "public/all/index.html"
write_neutral_html "public/results/index.html"
write_neutral_html "public/all_results/index.html"

cat > public/source_manifest.json <<EOF
{
  "status": "NO_PUBLIC_CONTENT",
  "message": "No public content available."
}
EOF

cat > public/source_audit.json <<EOF
{
  "status": "NO_PUBLIC_CONTENT",
  "message": "No public content available."
}
EOF

echo "===== FINAL PUBLIC FILES ====="
ls -R public
