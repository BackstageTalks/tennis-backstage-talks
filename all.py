def render_rss(predictions):
    now = datetime.now(timezone.utc)

    items = ""

    for prediction in predictions:
        pick = str(prediction.get("pick", "-"))
        opponent = str(opponent_of(prediction) or "-")
        match_time = str(match_time_of(prediction))
        odds = fmt(prediction.get("odds"))
        win = percent(prediction.get("probability"))

        title = html.escape(f"{pick} to win vs {opponent}")
        description = html.escape(
            f"Pick: {pick} | Opponent: {opponent} | Match time: {match_time} | Odds: {odds} | Win probability: {win}"
        )

        items += f"""
<item>
<title>{title}</title>
<description>{description}</description>
<pubDate>{format_datetime(now)}</pubDate>
</item>
"""

    return f"""<?xml version="1.0" encoding="UTF-8"?>
<rss version="2.0">
<channel>
<title>Backstage Talks Tennis All Matches</title>
<link>https://backstagetalks.github.io/tennis-backstage-talks/all/</link>
<description>All daily tennis predictions</description>
<lastBuildDate>{format_datetime(now)}</lastBuildDate>
{items}
</channel>
</rss>
"""


