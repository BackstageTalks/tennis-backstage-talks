def latest_file(pattern):
    files = sorted(glob.glob(pattern), reverse=True)
    return files[0] if files else None


def load_json(path, default):
    if not path:
        return default

    if not os.path.exists(path):
        return default

    with open(path, encoding="utf-8") as f:
        return json.load(f)


def load_predictions():
    path = latest_file("public/all_predictions_*.json")
    data = load_json(path, [])

    print("ALL JSON:", path, len(data))
    return data


def percent(value):
    try:
        return f"{float(value) * 100:.1f}%"
    except Exception:
        return "-"


def fmt(value):
    if value is None:
        return "-"
    return str(value)


def opponent_of(prediction):
    if prediction.get("opponent"):
        return prediction.get("opponent")

    pick = prediction.get("pick")
    player1 = prediction.get("player1")
    player2 = prediction.get("player2")

    if pick == player1:
        return player2

    return player1


def match_time_of(prediction):
    return (
        prediction.get("time")
        or prediction.get("match_time")
        or prediction.get("match_time_raw")
        or "TBD"
    )


def match_of(prediction):
    return (
        prediction.get("match")
        or f"{prediction.get('player1', '-')} vs {prediction.get('player2', '-')}"
    )


